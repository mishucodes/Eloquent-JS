//A brief description of the nature of this Language:
    //a) There are only 3 primitive data types: numbers, strings, & template literals.
        //aa) Numbers: signed integers;
        //ab) Strings: any sequence of characters b/w " ".
        //ac) Template Literals: any sequence of characters b/w ` `.
    //b) There are only 2 non-primitive data types: objects & arrays.
        //ba) Objects: any key-value pair b/w <>;
        //bb) Arrays: any sequence of values b/w [].
    //c) Basic Keywords in language:
        //ca) 'suppose': keyword to declare a non-const binding. the bindings are loosely typed.
        //cb) 'function': keyword to declare a function definition.
        //cb) 'if': like 'if' in JS.
        //cc) 'elseIf': like 'else if' in JS.
        //cd) 'else': like 'else' in JS.
    //d) Basic Operators in language:
        //da) 'assignment': ->
        //db) 'arithmetic': +, -, *, /, %
        //dc) 'logical': =, <, >, ==, !=, !==
    //d) Basic functions in language:
        //da) 'print': 'console.log' in JS.
        //db) all other JS functions are supported. Think of JS as a reliable proto language behind this one.
    //e) AS OF NOW, BAD CODE IS IGNORED, RATHER THAN PRODUCE A SYNTAX ERROR.