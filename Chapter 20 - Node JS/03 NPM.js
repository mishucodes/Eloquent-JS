//01. NPM: It's an online repository of JS modules, many of which are specifically written for Node. When you install Node on your computer, you also get the npm command, which you can use to interact with this repository. We can use NPM to fetch and install that package on our computer. For Example:
    //$ npm install package_name

//EXPLANATION:
    //a) NPM will first find/create a folder called "node_modules".
    //b) Inside that folder, NPM will find/create a new folder with the package's name that contains the library.
//SOME NOTES:
    //a) By default NPM installs packages under the current directory, rather than in a central place.
    //b) It puts each application in full control of the packages it installs and makes it easier to manage versions and clean up when removing an application.