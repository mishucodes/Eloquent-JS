//01. Internet: Basically two/more computers sharing information with one-another. Today, it means almost all computers on Earth being able to communicate with one-another.

//02. World-Wide-Web: A set of protocols & formats to allow us to visit web-pages through a web-browser. Example of a "website":
    //https://worldofmishu.in/index.html
        //a) https:// -> protocol (how to get)
        //b) worldofmishu.in -> server (where to get)
        //c) /index.html -> path to resource (what to get)

//03. HTML: It's a format of a document. These documents are called "web-pages". They can link to each-other.

//04. Sandboxing: Browsers strictly allow only certain permissions to all websites. For example, they can't really access files on your system, etc. It's quite surprising that governments have not yet demanded special treatment for their websites.


